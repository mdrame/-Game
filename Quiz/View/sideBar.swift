import Foundation
import UIKit


class SideBarButton {
    
    // UIFunctions ( In-order to not occupy viewDidLoad func )
       
    func uiButtonsDesigned(view:  UIButton ) {
           
           // sidde button designs
           view.layer.borderWidth = 3.0
           view.layer.borderColor = #colorLiteral(red: 1, green: 0.2256877422, blue: 0.0854042545, alpha: 1)
           view.layer.cornerRadius = 10.0
           
       }
    
    
    
    
    
    
    
}
